#' @title Squaring Numeric Data
#'
#' @description This function will return the input cubed. Can be numeric or vector.
#' @param x  numeric, vector, number(s) to be cubed
#' @returns numeric. vector
#'
#' @examples
#' cube(5)
#' cube(0)
#' cube(c(1,2,3))
#' cube(d)
#'
#' @export

cube <- function(x) {

  return(x^3) #returns x cubed
}

