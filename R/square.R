#' @title Squaring Numeric Data
#'
#' @description This function will return the input squared. Can be numeric or vector.
#' @param x  numeric, vector, number(s) to be squared
#' @returns numeric. vector
#' @examples
#' square(5)
#' square(0)
#' square(c(1,2,3))
#' square(d)
#'
#' @export


square <- function(x) {

  return(x^2) #returns x squared
  }

